 
package model;
 
public class Vulcao {
    
    private int id;
    private String nome;
    private String formadoPor;
    private String ultimaErupcao;
    private String status;
    private String coordenadas;
    private String pais;
    private String tipoLava;
    private String descricao;
    private String erupcaoVulcanica;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the formadoPor
     */
    public String getFormadoPor() {
        return formadoPor;
    }

    /**
     * @param formadoPor the formadoPor to set
     */
    public void setFormadoPor(String formadoPor) {
        this.formadoPor = formadoPor;
    }

    /**
     * @return the ultimaErupcao
     */
    public String getUltimaErupcao() {
        return ultimaErupcao;
    }

    /**
     * @param ultimaErupcao the ultimaErupcao to set
     */
    public void setUltimaErupcao(String ultimaErupcao) {
        this.ultimaErupcao = ultimaErupcao;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the coordenadas
     */
    public String getCoordenadas() {
        return coordenadas;
    }

    /**
     * @param coordenadas the coordenadas to set
     */
    public void setCoordenadas(String coordenadas) {
        this.coordenadas = coordenadas;
    }

    /**
     * @return the pais
     */
    public String getPais() {
        return pais;
    }

    /**
     * @param pais the pais to set
     */
    public void setPais(String pais) {
        this.pais = pais;
    }

    /**
     * @return the tipoLava
     */
    public String getTipoLava() {
        return tipoLava;
    }

    /**
     * @param tipoLava the tipoLava to set
     */
    public void setTipoLava(String tipoLava) {
        this.tipoLava = tipoLava;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the erupcaoVulcanica
     */
    public String getErupcaoVulcanica() {
        return erupcaoVulcanica;
    }

    /**
     * @param erupcaoVulcanica the erupcaoVulcanica to set
     */
    public void setErupcaoVulcanica(String erupcaoVulcanica) {
        this.erupcaoVulcanica = erupcaoVulcanica;
    }
    
    
}
